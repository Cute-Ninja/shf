<?php

namespace App\Domain\POPO;

class MailSenderPOPO
{
    /** @var string */
    protected $name;

    /** @var string */
    protected $email;

    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

    public function getEmail(): string
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail(string $email): void
    {
        $this->email = $email;
    }
}